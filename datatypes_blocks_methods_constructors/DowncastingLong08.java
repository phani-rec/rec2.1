class ExplicitLong02
{
 public static void main(String[] args)
 {
  long l2 = 1234567l;
  System.out.println("before conversion long : "+i2);   
  int i2=(int)l2;
  System.out.println("after conversion int : "+i2+" long  "+l2);
  short s2=(short)l2;
  System.out.println("after conversion short : "+s2+" long  "+l2);
  byte b2=(byte)l2;
  System.out.println("after conversion long : "+b2+" long  "+l2);     
 }
}